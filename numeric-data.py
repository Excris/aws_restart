print("Python has three numeric types: int, float, and complex")
#myvalue = 1 Criação da variável <int> inteiro = 1 
myvalue = 1
print(myvalue)
print(type(myvalue))
print(f"{myvalue} is of the data type {type(myvalue).__name__}")
#myValue = 3.14 valor PI, Criação da variável <Float> decimal = 3.14
myValue = 3.14
print(myValue)
print(type(myValue))
print(f"{myValue} is of the data type {type(myValue).__name__}")
#Myvalue = 5j valor Complex, Criação da variável <Complex> complex = 5j
Myvalue = 5j
print(Myvalue)
print(type(Myvalue))
print(f"{str(Myvalue)} is of the data type {type(Myvalue).__name__}")
#MyValue = true valor Bool, Criação da variável <Bolean> Bool = true ou false
MyValue = True
print(MyValue)
print(type(MyValue))
print(f"{str(MyValue)} is of the data type {type(MyValue).__name__}")
